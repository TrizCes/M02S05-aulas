using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace statica
{
  public class Calculadora
  {
    public static int Soma(int num1, int num2)
    {
      return num1 + num2;
    }

    public int Subtracao(int num1, int num2)
    {
      return num1 - num2;
    }
  }
}